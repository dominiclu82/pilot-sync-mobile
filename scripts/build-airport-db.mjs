// Build a compact airport database for Pilot Log Places / From-To names.
// Source: OurAirports (public domain) airports.csv → filter to IATA-coded + scheduled service.
// Output: src/spa/airport-db.js  →  var _PL_AIRPORTS = [[icao,iata,name,city,cc,lat,lon], ...]
// Run: node scripts/build-airport-db.mjs <path-to-airports.csv>
import { readFileSync, writeFileSync } from 'fs';
import { createRequire } from 'module';
const require = createRequire(import.meta.url);
const geomagnetism = require('geomagnetism');   // WMM 地磁模型 → 算各機場磁偏角（magnetic variation）
const geoModel = geomagnetism.model();

const csvPath = process.argv[2] || './_oa.csv';
const txt = readFileSync(csvPath, 'utf8');

function parseLine(l) {
  const out = []; let cur = '', q = false;
  for (let i = 0; i < l.length; i++) {
    const c = l[i];
    if (q) { if (c === '"') { if (l[i + 1] === '"') { cur += '"'; i++; } else q = false; } else cur += c; }
    else { if (c === '"') q = true; else if (c === ',') { out.push(cur); cur = ''; } else cur += c; }
  }
  out.push(cur); return out;
}

const lines = txt.split(/\r?\n/);
const H = parseLine(lines[0]); const x = {}; H.forEach((h, i) => x[h] = i);
const r3 = (v) => Math.round(Number(v) * 1000) / 1000;

// OpenFlights 補時區（IANA tz，如 Asia/Taipei）。用 ICAO 對應。下載：
// https://raw.githubusercontent.com/jpatokal/openflights/master/data/airports.dat → ./_of.dat
// 欄位：0 id,1 name,2 city,3 country,4 IATA,5 ICAO,6 lat,7 lon,8 alt,9 offset,10 dst,11 IANA tz,...
const tzByIcao = {};
try {
  const oft = readFileSync('./_of.dat', 'utf8').split(/\r?\n/);
  for (const l of oft) {
    if (!l) continue;
    const f = parseLine(l);
    const ic = (f[5] || '').trim().toUpperCase();
    const tz = (f[11] || '').trim();
    if (ic && ic !== '\\N' && tz && tz.includes('/')) tzByIcao[ic] = tz;
  }
  console.log('OpenFlights tz loaded:', Object.keys(tzByIcao).length);
} catch (e) { console.log('WARN: no ./_of.dat — tz column will be empty'); }

// OurAirports runways.csv → ICAO → 跑道方向 idents（如 05L/23R/05R/23L；去掉已關閉的、去重排序）。
// 下載：https://davidmegginson.github.io/ourairports-data/runways.csv → ./_rwy.csv
const rwyByIcao = {};
try {
  const rt = readFileSync('./_rwy.csv', 'utf8').split(/\r?\n/);
  const RH = parseLine(rt[0]); const rx = {}; RH.forEach((h, i) => rx[h] = i);
  for (let i = 1; i < rt.length; i++) {
    if (!rt[i]) continue;
    const f = parseLine(rt[i]);
    if (f[rx.closed] === '1') continue;             // 跳過已關閉跑道
    const ic = (f[rx.airport_ident] || '').trim().toUpperCase();
    if (!ic) continue;
    const le = (f[rx.le_ident] || '').trim();
    const he = (f[rx.he_ident] || '').trim();
    if (!rwyByIcao[ic]) rwyByIcao[ic] = [];
    // 空白座標欄要視為「缺」→ null，不可用 Number('') = 0 變成赤道座標（codex P1）。
    const coord4 = (s) => { s = (s == null ? '' : String(s)).trim(); if (s === '') return null; const n = Number(s); return isNaN(n) ? null : Math.round(n * 1e4) / 1e4; };
    const rlen = parseInt(f[rx.length_ft], 10), rwid = parseInt(f[rx.width_ft], 10);
    // [le_ident, he_ident, le_lat, le_lon, he_lat, he_lon, length_ft, width_ft]
    // 座標給地圖畫跑道線 + 標號碼（4 位 ~10m）；長寬給地圖標示尺寸 + 依風向算綠/橘端用
    rwyByIcao[ic].push([le, he,
      coord4(f[rx.le_latitude_deg]), coord4(f[rx.le_longitude_deg]),
      coord4(f[rx.he_latitude_deg]), coord4(f[rx.he_longitude_deg]),
      isNaN(rlen) ? null : rlen, isNaN(rwid) ? null : rwid]);
  }
  console.log('runways loaded for', Object.keys(rwyByIcao).length, 'airports');
} catch (e) { console.log('WARN: no ./_rwy.csv — runways column will be empty'); }

const rows = [];
for (let i = 1; i < lines.length; i++) {
  if (!lines[i]) continue;
  const f = parseLine(lines[i]);
  const iata = (f[x.iata_code] || '').trim().toUpperCase();
  if (!iata) continue;
  if (f[x.scheduled_service] !== 'yes') continue;
  let name = (f[x.name] || '').trim();
  if (name.startsWith('[Duplicate]')) continue;
  const icao = (f[x.icao_code] || '').trim().toUpperCase();
  const city = (f[x.municipality] || '').trim();
  const cc = (f[x.iso_country] || '').trim().toUpperCase();
  const lat = r3(f[x.latitude_deg]);
  const lon = r3(f[x.longitude_deg]);
  const tz = tzByIcao[icao] || '';
  const elev = f[x.elevation_ft] !== '' && f[x.elevation_ft] != null ? Math.round(Number(f[x.elevation_ft])) : '';
  const region = (f[x.iso_region] || '').trim();   // 如 US-CA（含國碼）
  // 跑道物件陣列：[le_ident, he_ident, le_lat, le_lon, he_lat, he_lon]，依跑道號數字排序
  const rwys = rwyByIcao[icao]
    ? rwyByIcao[icao].slice().sort((a, b) => (parseInt(a[0], 10) || 0) - (parseInt(b[0], 10) || 0))
    : [];
  let mvar = null;   // 磁偏角（度，正=E 偏東 / 負=W 偏西），WMM 模型依座標算
  try { mvar = Math.round(geoModel.point([Number(f[x.latitude_deg]), Number(f[x.longitude_deg])]).decl * 10) / 10; } catch (e) {}
  rows.push([icao, iata, name, city, cc, lat, lon, tz, elev, region, rwys, mvar]);
}
// sort by ICAO then IATA for stable diffs
rows.sort((a, b) => (a[0] + a[1]).localeCompare(b[0] + b[1]));

const banner = '// AUTO-GENERATED by scripts/build-airport-db.mjs from OurAirports (public domain).\n' +
  '// ' + rows.length + ' airports. Cols: [ICAO, IATA, name, city, country, lat, lon, tz, elev_ft, region, runways[[le,he,leLat,leLon,heLat,heLon,lenFt,widFt]], mag_var].\n' +
  '// Re-generate: download https://davidmegginson.github.io/ourairports-data/airports.csv then run the script.\n';
const body = 'var _PL_AIRPORTS=[\n' +
  rows.map(r => JSON.stringify(r)).join(',\n') +
  '\n];\n';
writeFileSync('./src/spa/airport-db.js', banner + body, 'utf8');
console.log('wrote src/spa/airport-db.js — ' + rows.length + ' airports, ' + (banner.length + body.length) + ' bytes');
